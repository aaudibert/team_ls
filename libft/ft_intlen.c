int					ft_intlen(int nbr)
{
	int	len;

	len = 1;
	if (nbr < 0)
	{
		nbr *= -1;
		len++;
	}
	while (nbr > 9)
	{
		len++;
		nbr = nbr / 10;
	}
	return (len);
}
