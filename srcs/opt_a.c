#include "../inc/ft_ls.h"

int		*g_flags;

int		opt_a(t_file *dir)
{
	if (g_flags[FLAG_A] == 1 && dir->f_name[0] == '.')
		return (1);
	else if (dir->f_name[0] != '.')
		return (1);
	return (0);
}
